        // Inflate the layout for this fragment
        val view = inflater.inflate(R.layout.fragment_questions, container, false)
        val textView= view.findViewById<TextView>(R.id.text_view_question_count)
        textView.setText(arguments?.getString("questionText"))

        val letsDoItButton = view.findViewById<Button>(R.id.button_lets_do_it)

        letsDoItButton.setOnClickListener {
            val assignmentModelList: ArrayList<AssignmentModelX>? = arguments?.getParcelableArrayList("assignmentModelList")
            val intent = Intent(activity, QuestionsFragment::class.java)
            intent.putExtra("questions", assignmentModelList as Serializable)
            context?.startActivity(intent)
            (context as Activity).finish()
        }
        return view
    }

